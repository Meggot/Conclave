    private void removeRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeRequestActionPerformed
        String request = testField.getText();
        if (request != null && dlm.contains(request)) {
            agent.removeRequest(request);
            dlm.removeElement(request);
            output.setText("You have removed a request.");
        }
        output.setText("You must enter the exact request.");
    }//GEN-LAST:event_removeRequestActionPerformed

    private void createRequestButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createRequestButtonActionPerformed
        String request = testField.getText();
        if (request != null) {
            dlm.addElement(request);
            agent.addRequest(request);
        }
    }//GEN-LAST:event_createRequestButtonActionPerformed

    private void beginTestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beginTestActionPerformed
        String filenameS = filename.getText();
        int iterationsI = Integer.parseInt(iterations.getText());
        System.out.print(agent.allRequests());
        if (filenameS != null && iterationsI > 0) {
            agent.createNewTest(filenameS);
            SwingWorker worker = new SwingWorker() {
                private boolean testFinished = false;
                
                @Override
                protected Object doInBackground() throws Exception {
                    output.setText("Starting test...");
                    testFinished = agent.beginTest(iterationsI);
                    return null;
                }

                @Override
                protected void done() {
                    output.setText("Test passed? " + testFinished);
                }
                
            };
            worker.execute();
        } else {
            output.setText("You must enter a valid filename and a iterations count.");
        }
    }//GEN-LAST:event_beginTestActionPerformed


